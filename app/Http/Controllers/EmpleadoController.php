        $empleado->nombreEmpleado= $request->post('nombreEmpleado');
        $empleado->passwordEmpleado= $request->post('passwordEmpleado');
        $empleado->categoriaEmpleado= $request->post('categoriaEmpleado');
        
        $empleado->save();
        
        $empleado->response()->json('$empleado','200');
    }

