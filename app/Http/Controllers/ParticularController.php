     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $particular = new Particular();
        
        $particular->nombreParticular=$request->input('nombreParticular');
        $particular->rutParticular=$request->input('rutParticular');
        $particular->passwordParticular=$request->input('passwordParticular');
        $particular->direccionParticular=$request->input('direccionParticular');
        $particular->emailParticular=$request->input('emailParticular');
        
        $particular->save();
        
        return response()->json($particular,200);
    }

